    public void crearOpciones(){
        nodoMateria r=v1.getA().getP();
        String siglas[]=new String[v1.getA().nroElementos()];
        int i=0;
        while(r!=null){
            siglas[i]=r.getX().getSigla();
            i++;
            r=r.getSig();
        }   
        ComboBoxModel<String> op =new DefaultComboBoxModel<String>(siglas);
        siglasOp.setModel(op);
    }
    

