    private void siglasOpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_siglasOpActionPerformed
        // TODO add your handling code here:
        //mostrar el index
        int posicion=siglasOp.getSelectedIndex();
        int i=0;
        nodoMateria r1=v1.getA().getP();
        while(r1!=null){
            if(i==posicion){
                String titulos[]={"CI", "NOTA"};
                DefaultTableModel pantalla=new DefaultTableModel(null,titulos);
                nodoCalificacion r2=r1.getX().getLSC().getP();
                while(r2!=null){
                    String fila[]={Integer.toString(r2.getX().getCi()),Integer.toString(r2.getX().getNota())};
                    pantalla.addRow(fila);
                    r2=r2.getSig();
                }                
                pantalla_interfaz.setModel(pantalla);        
            }
            i++;
            r1=r1.getSig();
        }
    }//GEN-LAST:event_siglasOpActionPerformed

